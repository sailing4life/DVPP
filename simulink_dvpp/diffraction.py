def _interp(arr, omega_e):
    return float(np.interp(omega_e, _OMEGA_AR, arr))


def diffraction_force(wave, eta, eta_dot, time, count_sum):
    """
    Port of Simulink chart_457 `Diffraction force`.
    """
    g = GRAVITY
    X = eta[0]
    Y = eta[1]
    factor = 1.0 + ((count_sum / GRAVITY) - 12436.0) / 12436.0

    F_d2 = F_d3 = F_d4 = F_d5 = 0.0

    for w_data in np.asarray(wave, dtype=float):
        zeta_a, k, nu, omega = w_data
        if omega == 0.0:
            continue

        # Encounter frequency: ω_e = ω − ω²·(u·cos μ + v·sin μ)/g
        # Both surge (u) and sway (v) project onto the wave direction (thesis §4.6.2).
        V_proj = eta_dot[0] * np.cos(nu) + eta_dot[1] * np.sin(nu)
        omega_e = omega - (omega**2 * V_proj) / g
        omega_e = min(4.95, max(0.14, abs(omega_e)))
        phase = k * X * np.cos(nu) + k * Y * np.sin(nu) - omega * time

        F_d2 += zeta_a * _interp(_F2, omega_e) * np.cos(phase + _interp(_P2, omega_e))
        F_d3 += zeta_a * _interp(_F3, omega_e) * np.cos(phase + _interp(_P3, omega_e))
        F_d4 += zeta_a * _interp(_F4, omega_e) * np.cos(phase + _interp(_P4, omega_e))
        F_d5 += zeta_a * _interp(_F5, omega_e) * np.cos(phase + _interp(_P5, omega_e))

    return np.array([0.0, F_d2, F_d3, F_d4, F_d5, 0.0]) * factor
